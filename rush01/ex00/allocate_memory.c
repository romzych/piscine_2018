#include <stdlib.h>

void	allocate_memory(int ***grid)
{
	int i;

	i = 0;
	*grid = (int **)malloc(9 * sizeof(int *));
	while (i < 9)
	{
		(*grid)[i] = (int *)malloc(9 * sizeof(int));
		i++;
	}
}
