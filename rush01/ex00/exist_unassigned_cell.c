int		exist_unassigned_cell(int **grid, int *i, int *j)
{
	*i = 0;
	*j = 0;
	while (*i < 9)
	{
		*j = 0;
		while (*j < 9)
		{
			if (grid[*i][*j] == -2)
				return (1);
			(*j)++;
		}
		(*i)++;
	}
	return (0);
}
